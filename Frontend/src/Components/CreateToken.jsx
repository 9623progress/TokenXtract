const CreateToken = () => {
  const [token, setToken] = useState("");
  const [account, setAccount] = useState(null);
  const handleTokenChange = (e) => setToken(e.target.value);

  const HandleCreateToken = async () => {
    try {
      if (!window.ethereum) {
        toast.error("üö® Please install MetaMask!");
        return;
      }

      await window.ethereum.request({ method: "eth_requestAccounts" });
      const provider = new ethers.BrowserProvider(window.ethereum);
      const signer = await provider.getSigner();
      const userAddress = await signer.getAddress();
      setAccount(userAddress);

      const contract = new ethers.Contract(contractAddress, contractABI, signer);

      // ‚úÖ Check if user is the owner
      const contractOwner = await contract.owner();
      console.log("Contract Owner:", contractOwner);
      console.log("Connected Wallet:", userAddress);

      if (contractOwner.toLowerCase() !== userAddress.toLowerCase()) {
        toast.error("‚ùå Only the government (contract owner) can mint tokens!");
        return;
      }

      // ‚úÖ Call mintTokens correctly
      const tx = await contract.mintTokens(ethers.parseUnits(token, 0)); 

      await tx.wait();
      toast.success("‚úÖ Tokens minted successfully!");
      setToken("");

    } catch (error) {
      console.error("Minting error:", error);
      toast.error(error.reason || "‚ùå Transaction failed!");
    }
  };

  return (
    <div className="create-token-top-div">
      <div className="create-token-box">
        <div className="create-token-input">
          <label htmlFor="create-token">Enter Token Amount (in Rs): </label>
          <input
            id="create-token"
            type="number"
            placeholder="Enter Amount in Rs"
            value={token}
            onChange={handleTokenChange}
            min="1"
          />
        </div>
        <button className="create-token-button" onClick={HandleCreateToken}>
          Create Tokens
        </button>
      </div>
    </div>
  );
};

export default CreateToken;